IF OBJECT_ID('dbo.Albums', 'U') IS NOT NULL DROP TABLE dbo.Albums; 
IF OBJECT_ID('dbo.Artists', 'U') IS NOT NULL DROP TABLE dbo.Artists; 

CREATE TABLE Artists
(
	Id uniqueidentifier NOT NULL PRIMARY KEY,
	Name nvarchar(max) NOT NULL
);

CREATE TABLE Albums
(
	Id uniqueidentifier NOT NULL PRIMARY KEY,
	Title nvarchar(max) NOT NULL,
	Artist_Id uniqueidentifier NOT NULL,

	CONSTRAINT FK_ArtistAlbum FOREIGN KEY(Artist_Id) REFERENCES Artists(Id)
);

